    By the end of that season, I went from my slowest ever college time to a
    conference title and a third place finish at nationals. I realize how lucky
    I am to be able to say that because things do not work out like this for
    everyone. The question of long-term contentment is much more the focus to
    the person who gets chronic stress fractures and is unable to train for half
    of their career. We all fight our own battles and come away with unique
    nuggets of wisdom.
</p>

<p>
    That year was an emotional and performance climax of college running for me.
    This was in no small part because the takeaways from that season were still
    processing and the philosophy that informed training and lifestyle was far
    from perfect. After graduating, starting my job search, and moving back
    home, running resumed its supportive role in life by providing me with
    community as well as something to wake up to each ady. There was no
    template, no more running career ambitions, it was now just a hobby that I
    continued to do for enjoyment.
</p>

<p>
    After moving to Seattle and starting to work, I have more or less continued
    to enhance the quality of my engagements with running as a hobby. The local
    performance running group Club Northwest provides a majority of the training
    resources and community that I am now part of. While it could just be me
    running out there in the rain, I concede now that running is something that
    is better done with others. The cherry on top is that my ability and skills
    have continued to develop despite no longer having the intensity that I once
    believed was necessary to be good. The long career in this sport is
    evidently a result of consistency and continued enjoyment of putting in the
    effort, balancing the load, and stepping back once in a while to appreciate
    the way you&rsquo;ve come.
</p>

<p>
    The metaphor in this title still needs to be mentioned one last time. Running had
    a very influential, almost dominating effect on my life, and it is now just
    something that I put time into more or less at-will. A lot of us fear
    commitment as this thing that will have a drastic and uncontrollable impact
    on our lives, which is true if we are inexperienced and fail to understand
    what these effects are in advance. Being able to think critically about
    these impacts is what lets us decide whether a thing or person is worth
    investing this much time in. It is not the level of commitment or the amount
    of time that should scare us and instead how the results differ from our
    goals or values. Paradoxically, the things that can shape life like this are
    also the most rewarding. When I think of all the places that running has
    taken me and all of the paths that could have been explored, I find that
    faith in myself and honest judgement tends to work out for the right ones. There
    will always be an opportunity to drink Kool-Aid, and if you choose to, make sure
    you are doing it for the right reasons.
</p>


    Nonetheless, that half a year maintains in my mind as the perfect season&mdash;one
    without flaws, worry and just growth. People sometimes say that there is no growth
    without struggle, and even after having seen both experiences, I am uncertain
    whether it is one or the other. Come Spring a few weeks into the start of our
    season, we get kicked off of campus with two-days&rsquo; notice, and before that,
    I remember sitting on a bench with all 50 or 60 of my teammates, getting word
    from our athletic director that we would no longer be in person teammates for
    that season. I can&rsquo;t remember feeling more emotional in my life with a
    group of people I had only really known for six months. None of us at the time
    knew that our lives would be placed on hold for years. None of us knew how much
    the world was going to change once it all started back up again. In those two
    days, I think my gut feel was as true as it&rsquo;s ever been. The world and
    running were bound to change for me, and for everyone.
</p>

<p>
    Moving back home and finishing up that semester, keeping the runs in just
    for mental clarity and little else rapidly changed my relationship with the
    sport. There were no highs of winning, there were no lows of the team having
    a shitty day, just the sound of my feet on the completely empty bike path
    and the miles ticking up. Running probably allowed me to exist without
    depression in this state, but I think it also masked the reality of what
    everyone in college at this time was losing&mdash;the ability to connect
    with physically present people. This is where I understand that running can
    be dangerous and that it allows people to isolate like this in a very
    peaceful state. It remains something that I remind myself of when a
    day-to-day life of only office work and solo runs doesn&rsquo;t seem that
    initially harmful.
</p>

<p>
    One day, I was working out at a track near my family&rsquo;s home when an
    older guy wearing Ray-Bans and face cloaked in a cotton mask approached me
    asking if I was training for something. His name was Ed Italo, coach at the
    Thatcher School in the small, Los Angeles getaway town of Ojai, CA. I also
    recognized him from our league meets a couple years back. His athlete, Colin
    Kirkpatrick, had been a nemesis of mine on the track. Ed would coach Colin
    to sit back throughout the race and and then pass in the last hundred
    meters, a brilliantly simple race tactic but not one that I particularly
    appreciated at the time. Ed was a cool guy though. He had two sons, he
    played Burning Man sets as he rolled around in an FJ Cruiser, and he had a
    vastly different perspective on running than any of my past coaches. He made
    me aware of this over the following two years that he on-and-off coached me.
    In Ed&rsquo;s frame of things, track is a perfectionist&rsquo;s sport.
    Obsess over everything you can and do everything in a particular way and you
    will be good. You will be <i>the best</i>.
</p>

<p>
    Running with Ed was a great experience and there are absolutely kids who
    have grown by buying into it, but for me the Kool-Aid became tough to drink
    after a few years. There were superficial things about the training that did
    not click. My then five-year background was primarily in longer-distance
    events while his vision of meaningful running only extended to four laps
    around the track. The actual content of his training consisted almost
    entirely of speed strength and no endurance development. However, the main
    friction point between us was caused by my emerging value of balance.
    Ed&rsquo;s philosophy about being a great athlete is a very easy conclusion
    to draw from the small sample size of only the most successful people in
    each field or pursuit. But I think it is a false conclusion given the
    reality that there are more people in the world than things to be the best
    at. However it was becoming evident that I never did my best when I thought
    this way about my goals. I often performed best under the condition of
    balance. I believe it is very likely for this to apply to the vast majority
    of people who achieve mastery in something. Balance and optimality are in
    many ways equivalent and I realized that although I wasn&rsquo;t trying to
    pursue being the best in the world, I was willing to relentlessly pursue my
    own potential. I was simply not willing to risk my fulfillment and love that
    I had built for this sport to test Ed&rsquo;s theory.
</p>

<p>
    Over the semesters that we were not on campus, my friends and I lived in
    Pagosa Springs, Colorado and Eugene, Oregon partially because they were
    great places to run also partially because we had no idea how much rain we
    were going to get during the tail end of Oregon winter. It seemed that my
    friends had built very different relationships with running during their
    first few months of remote school. Some had started running trails as a
    means of observing natural beauty. Others had clearly found that they just
    couldn&rsquo;t run alone, but I was still very embedded in the habit of
    running to isolate, or perhaps to comfort myself from the feeling of
    isolation. This period made a lot of good runners exceptional, and probably
    made others realize that there were more enjoyable things in life.
</p>

<p>
    The running world had changed in so many ways we got back to our first
    practice of 2021. Goldhammer had retired since his last hurrah over a year
    ago and in his place stood what some might call his inverse&mdash;a woman
    who didn&rsquo;t say much beyond a few directions in an Eastern European
    accent to clarify that days&rsquo; workout. Getting to know Marina, our new
    coach, was awesome. Super shoes had hit the consumer running market
    overnight and records were starting to fall left and right. And I started
    approaching the sport with a new level of ambition, inspired by Ed&rsquo;s
    promise of greatness.
</p>

<p>
    This was the season where I discovered what it means to believe in myself.
    To my frustration, I started running some of my slowest college times after
    having trained harder than ever before. Pushing through took a leap of faith
    against the sadness and resignation that I felt towards something that I
    loved dearly. What was important in this situation was just taking a step
    back to collect thoughts and feelings. My gut instinct of training harder
    would have only worsened the problem, which I later discovered was iron
    deficiency. My alternative zero-thought response of quitting the team that
    season altogether was just as entitled. It took a few hard conversations
    with Marina and some doctors visits to set aside my pride and just finish
    the season as best as I could. To my pleasant surprise, things turned
    around. And they didn&rsquo;t just turn around; they changed fundamentally. <figure

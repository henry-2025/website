<p>
    <strong>I</strong> want to think that my running story begins long before I actually
    ever ran without the intention of getting from one place to another. 10-year-old
    me got to observe my mom&rsquo;s daily routine of running to work through the
    SoCal wilderness and then flying to Boston for some weird event once each year.
    I think I tried a Gu gel or two and got the sense that light shoes with vibrant
    colors and clothes made out of dri-fit material were an important criteria for
    success, but beyond that I mostly learned that the sport was a large part of
    her identity. I came to associate the sport with her strengths&mdash;honesty,
    consistency, and kindness. It was only until after getting to college when all
    of my running nerd friends made me aware of the huge athletic pedigree that I
    had supporting my career, so thanks for the help and inspiration, Mom.
</p>


<p>
    Relatively recently, I also learned that my dad swam in college. He brings
    way more athletic credit to the table than I have so far recognized him for.
    It&rsquo;s always been in the back of my mind to eventually switch over to
    the pool, but there are a few more boxes on my running bucket list that I
    need to check before I commit to that. Thanks, Pop.
</p>

<p>
    Every sport that I played while growing up was frustrating as hell. At the
    time, I think this came from some lacking skill in temper management coupled
    with the unrealistic expectation that I had to be the best in everything
    that I picked up. Not being from a family that watched professional sports
    also left me with no higher inspiration or general love for the sports that
    I tried doing. There was a Little League summer, a basketball summer, many
    seasons on the middle school surf team, and then finally tennis by the end
    of middle school. All these were trending in the direction of teaching me
    that it&rsquo;s OK to be average and maybe just enjoy doing the sport as a
    physical activity or by relishing in your own improvement from one year to
    the next.
</p>

<p>
    As a freshman in high school, my mom signed me up for a cross country
    season. I don&rsquo;t think she&rsquo;s ever forced me to do anything like
    most moms would. She absolutely has a way of guilt tripping me into doing
    things and this was certainly one of those instances. I can still remember
    her saying &ldquo;come on, it&rsquo;ll be really fun. Look at how much I
    enjoy it!&rdquo; and thinking that there&rsquo;s no way it would be cool to
    do something that parents view as entertaining. <figure>
        <img src="10-years-running/hoka.jpg" alt="first race ever" />
        <figcaption>
            Picture of my first race ever, probably my best race ever
        </figcaption>
    </figure>
    The following two weeks of summer training served to prove me right as I clopped
    around in the two-sizes-too-large used running shoes that my dad said would serve
    me well, got some light shin splints, and experienced soreness for the first
    time in my life. We ran maybe 3 miles a day, suffered through drills in the mid-afternoon
    summer heat, and I developed a new appreciation for the single chilled water
    fountain in the high school gym. I quickly developed a sense for my talent though.
    And I try very hard to hold onto this memory for when I try new things now. I
    think it&rsquo;s good to remember what it&rsquo;s like for something to feel
    natural. Not everything is like that and it is good to understand what those
    things are. I ended up winning pretty much every Frosh-soph race I entered and
    immediately dropped out of the tennis team, which I replaced with track. I finished
    my freshman year, almost making CIF Southern Section Finals in the 1600m. So
    yes, trying to disprove my mom&rsquo;s point about running being fun became harder
    and harder each week. Unfortunately, this was mostly because this was the first
    time I had experienced what I was like to win at something.
</p>

<p>
    I ended up dropping more things to focus on sports over the next year of
    high school. I stop playing in jazz band. I interacted less with friends who
    did not run. I started to see my coach and older teammates as better mentors
    and role models than teachers and fellow classmates. I can see why this was
    concerning to my dad, who I think has a very neutral perspective on the
    sport and maybe a negative one on sports in general from his past
    experiences. With so much time going into one activity, it is important to
    consider how that activity shapes you. If you&rsquo;re drinking Kool-Aid
    with the wrong people, it can be lethal.
</p>

<p>
    The rest of high school can mostly be ignored except for junior year when
    something happened that gave me a new sense of attachment to the sport. I
    ran the Arcadia invitational that year in one of their B-tier 3200 heats and
    set up pretty impressive PR off of following and out-kicking a sophomore
    runner from Newbury Park by the name of Nico Young. <figure
        style="float: right"

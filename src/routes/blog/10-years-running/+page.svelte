    He ended up taking the state title in that event as a sophomore later that 2017
    season, granting me major bragging rights to all of my high school teammates
    at the time. My goal was to come back the following year and give him a run for
    his money. I think that&rsquo;s about the most that I&rsquo;ve overestimated
    my athletic abilities to date. The significance of that achievement has compounded
    over time as he&rsquo;s progressed to being the current American record holder
    in the 10,000 m and member of Team USA in 2024.
</p>

<p>
    That same year, a coach named John Goldhammer reached out to me asking if I
    wanted to visit his team at the Claremont Colleges in eastern LA. After a
    two-day recruiting trip spending time with several of his lunatic athletes
    who mostly went to Claremont McKenna College, I was totally sold on going to
    school in Claremont to run miles and drink beers. The unfortunate oversight
    I made was in applying to get my engineering degree at <i
        >Harvey Mudd College</i
    >, which lies just one block North of Claremont McKenna. Social skills here
    are less abundant than I experienced on my tour, but people there still run
    lots of miles and drink beers. In the end, I guess it all worked out and
    that ended up scheduling the next four years of my running career. This
    started in Fall of 2019 when the world was so innocent, the tech job market
    was unperturbed by global economic shifts, and most Americans had probably
    never engaged in mortal combat over a roll of toilet paper. I remember being
    intimidated by my teammates who were mostly faster and better looking than I
    was and by coach Goldy, with his growing track record of fake-strangling his
    athletes.
</p>

<p>
    I set some impressive marks that season and got a taste of what I
    would&rsquo;ve learned from my college experience had things continued
    without a global pandemic. From my perspective, the team was together, the
    team was successful, and the mentorship of my seniors was the primary reason
    for my improvement over those four months of racing. People who were much
    faster than me seemed to acknowledge my potential. The casual &ldquo;good
    job out there today, Big Dog&rdquo; or nod from the seniors at team
    functions made me feel like they cared about both our friendship and my
    success in their group. Not one or the other. I can imagine this is a major
    frustration for anyone on this team who is now in the professional world. It
    can&rsquo;t be like this in a competitive, partially zero-sum game.
    It&rsquo;s also surprising to me that this seemingly can&rsquo;t exist
    without devoting your entire life to a single activity&mdash;to drink the
    Kool-Aid, so to say. I&rsquo;m simultaneously very grateful to had this
    experience, but also frustrated that I have this to compare to many future
    teams I will work on or compete with.

